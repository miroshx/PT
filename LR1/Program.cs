/*

 Разработать в среде Visual Studio в соответствии со своим вариантом
консольную линейную программу. Программа должна вычислить и вывести
на экран значение функции f(x) для трех разных аргументов, которые могут
принимать значение с точностью до двух знаков после запятой. Все исходные
данные (значение x) вводятся с клавиатуры, а f(x) равно:

 -----------------------------------------------------------------------

 x^2−ln|x+1|−3

 */


class Program
{
    static void Main(string[] args)
    {

        // Ввод и обработка трех аргументов
        for (int i = 1; i <= 3; i++)
        {
            Console.Write($"Введите значение аргумента {i}: ");
            if (double.TryParse(Console.ReadLine(), out double x))
            {
                // Вычисление f(x) = x^2 - ln|x + 1| - 3
                double result = Math.Pow(x, 2) - Math.Log(Math.Abs(x + 1)) - 3;

                // Округление до 2 знаков после запятой и вывод результата
                Console.WriteLine($"f({x}) = {Math.Round(result, 2)}");
            }
            else
            {
                Console.WriteLine("Некорректный ввод.");
            }
        }
    }
}